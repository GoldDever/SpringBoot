package com.example.SpringBoot.amigo;

public class AmigoClass {
    private String name;
    private int age;
    private boolean gender;

    public String getName() {
        return name;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public int getAge() {
        return age;
    }

    public boolean isGender() {
        return gender;
    }
}
